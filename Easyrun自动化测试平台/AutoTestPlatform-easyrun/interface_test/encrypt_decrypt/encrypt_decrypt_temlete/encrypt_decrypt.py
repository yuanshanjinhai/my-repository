class EncryptAndDecrypt():
    def __init__(self,aim_str):
        self.aim_str = aim_str

    def encryp(self): # 加密代码
        aim_str_encrypt = self.aim_str
        return aim_str_encrypt

    def decrypt(self): # 解密代码
        aim_str_decrypt = self.aim_str
        return aim_str_decrypt